function makeMsg(
	id: string,
	contactId: string,
	content: string,
	minutesAgo: number,
	isSent: boolean,
	status: Message["status"] = "read",
): Message {
	return {
		id,
		contactId,
		content,
		timestamp: new Date(Date.now() - minutesAgo * 60 * 1000),
		isSent,
		status,
		type: "text",
	};
}

